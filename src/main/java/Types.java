/**
 * Lernziel: Arten von Datentypen
 * - Primitive Typen
 * - arithmetische Typen
 * - Wahrheitswerte
 * - Referenztypen
 *
 * @see VariableDeclarations
 */
public class Types {
  /*
   * Die Datentypen in Java zerfallen in zwei Kategorien:
   *
   * - Primitive Typen
   *   Die primitiven (einfachen) Typen sind fest in der Sprache Java eingebaute
   *   Datentypen für Zahlen, Unicode-Zeichen und Wahrheitswerte.
   *   Bsp. char, byte, short, int, long, float, double, boolean
   *
   * - Referenztypen: Ein "Verweis" auf ein Objekt. Ist der Typ dieses Verweises
   *   Reference types refer to objects, e.g. of type String, String[], Scanner, System
   *   Mit diesem Datentyp lassen sich Objektverweise etwa auf Zeichenketten,
   *   Datenstrukturen oder Zwergpinscher verwalten.
   *
   * In Java gibt es zwei Arten eingebauter primitiver Datentypen:
   *
   * - arithmetische Typen
   *   1. Ganze Zahlen (byte, short, int, long, char)
   *   2. Fließkommazahlen (float, double)
   *   3. Unicode-Zeichen (char)
   *
   * - Wahrheitswerte type boolean
   *   für die Zustände true und false
   *
   *  - Standard in Java:
   *         Schreibe Ganzahl habe int
   *         Schreibe Ganzahl mit L habe long
   *         Schreibe Kommazahl habe double
   *
   *  - Werden im Zweierkomplement mit VZ gespeichert:
   *  byte 8 bits           -128 to 127        1000_0000      -2^7 to 2^7 -1
   *  short 16 bits         -2^15 to 2^15 - 1
   *  int 32 bits           -2^31 to 2^31 - 1
   *  long 64 bits          -2^63 to 2^63 - 1
   *
   *  - Keine Speicherung im Zweierkomplement
   *  char 16 bits            65 ist 'A' und 91 ist 'Z' und 97 ist 'a' und 122 ist 'z'
   *  float 32 bits           Single-precision 32-bit IEEE 754 floating point
   *  double 64 bits          Single-precision 64-bit IEEE 754 floating point
   *
   */
}
